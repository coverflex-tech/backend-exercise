  use Coverflex.BenefitsWeb, :controller
  
  alias Coverflex.Benefits.UserModel
  alias Coverflex.Benefits.ProductModel
  alias Coverflex.Benefits.Money

  def show(conn, %{"user_id" => user_id}) do
    user = case UserModel.get(user_id) do
        {:ok, user} -> user
        {:error, :not_found} -> 
            UserModel.create(user_id, rand_name(), 50000)
    end
    product_ids = ProductModel.get_by_user(user_id)
                  |> Enum.map(&(&1.id))
    json(conn, %{"user" =>
                 %{"user_id" => user.id, 
                   "data" =>
                   %{"balance" => Money.json_repr(user.vallet_balance), 
                     "product_ids" => product_ids}}})
  end
  
